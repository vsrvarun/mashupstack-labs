# Write a Python program to get the key, value and item in a dictionary.
#  eg:dict_num = {1: 10, 2: 20, 3: 30, 4: 40, 5: 50, 6: 60}
dict_num = {1: 10, 2: 20, 3: 30, 4: 40, 5: 50, 6: 60}
key = dict_num.keys()
value = dict_num.values()
item = dict_num.items()
print(dict_num)
print("The keys of dict_num:", key)
print("The values of dict_num:", value)
print("The items of dict_num:", item)